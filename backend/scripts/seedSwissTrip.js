const mongoose = require('mongoose');
const User = require('../models/User');
const Trip = require('../models/Trip');
const dotenv = require('dotenv');

dotenv.config();

const MONGODB_URI = process.env.MONGODB_URI || 'mongodb://127.0.0.1:27017/wayfarer';

