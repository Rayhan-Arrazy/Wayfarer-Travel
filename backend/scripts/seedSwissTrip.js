        { title: 'Arrive in Zurich', time: '10:00', date: new Date('2026-06-15'), location: 'Zurich Airport', checked: false },
        { title: 'Train to Grindelwald', time: '14:00', date: new Date('2026-06-15'), location: 'Interlaken Ost', checked: false },
        { title: 'Jungfraujoch Sphinx Observatory', time: '09:00', date: new Date('2026-06-17'), location: 'Jungfraujoch', checked: false }
